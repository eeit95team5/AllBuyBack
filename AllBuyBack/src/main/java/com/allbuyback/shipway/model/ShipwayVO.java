package com.allbuyback.shipway.model;

public class ShipwayVO {
	private int sw_id;
	private String sw_name;
	private int sw_price;
	
	public int getSw_id() {
		return sw_id;
	}
	public void setSw_id(int sw_id) {
		this.sw_id = sw_id;
	}
	public String getSw_name() {
		return sw_name;
	}
	public void setSw_name(String sw_name) {
		this.sw_name = sw_name;
	}
	public int getSw_price() {
		return sw_price;
	}
	public void setSw_price(int sw_price) {
		this.sw_price = sw_price;
	}
	
}
